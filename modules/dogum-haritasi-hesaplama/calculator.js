function hcDogumHaritasiHesapla() {
    var tarih = document.getElementById('hc-dhh-tarih').value;
    var saat = document.getElementById('hc-dhh-saat').value;
    var il = document.getElementById('hc-dhh-il').value;
    var utcFarki = parseFloat(document.getElementById('hc-dhh-utc').value);

    if (!tarih || !saat || !il || isNaN(utcFarki)) {
        alert('Lütfen doğum tarihi, saati, ili ve UTC farkını seçin.');
        return;
    }

    var koordinatlar = il.split(',');
    var enlem = parseFloat(koordinatlar[0]);
    var boylam = parseFloat(koordinatlar[1]);

    if (isNaN(enlem) || isNaN(boylam)) {
        alert('Seçilen il için konum bilgisi okunamadı. Lütfen tekrar deneyin.');
        return;
    }

    var tarihParca = tarih.split('-');
    var saatParca = saat.split(':');
    var yil = parseInt(tarihParca[0], 10);
    var ay = parseInt(tarihParca[1], 10);
    var gun = parseInt(tarihParca[2], 10);
    var saatDegeri = parseInt(saatParca[0], 10);
    var dakika = parseInt(saatParca[1], 10);

    if (yil < 1900 || yil > 2100) {
        alert('Bu hesaplayıcı 1900-2100 yılları arasındaki tarihler için uygundur.');
        return;
    }

    var jd = hcDhhJulianDay(yil, ay, gun, saatDegeri, dakika, utcFarki);
    var d = jd - 2451543.5;
    var dunya = hcDhhDunya(d);
    var gmst = hcDhhGmst(jd);
    var lst = hcDhhNormalize(gmst + boylam);
    var yerlesimler = [
        hcDhhKonum('Güneş', dunya.lon),
        hcDhhKonum('Ay', hcDhhAyBoylami(jd)),
        hcDhhKonum('Yükselen', hcDhhAscendant(lst, enlem)),
        hcDhhKonum('Merkür', hcDhhGezegenBoylami('Merkür', d, dunya)),
        hcDhhKonum('Venüs', hcDhhGezegenBoylami('Venüs', d, dunya)),
        hcDhhKonum('Mars', hcDhhGezegenBoylami('Mars', d, dunya)),
        hcDhhKonum('Jüpiter', hcDhhGezegenBoylami('Jüpiter', d, dunya)),
        hcDhhKonum('Satürn', hcDhhGezegenBoylami('Satürn', d, dunya))
    ];
    var elementSayilari = { 'Ateş': 0, Toprak: 0, Hava: 0, Su: 0 };
    var nitelikSayilari = { 'Öncü': 0, Sabit: 0, 'Değişken': 0 };
    var tablo = '';
    var siniraYakin = [];

    for (var i = 0; i < yerlesimler.length; i++) {
        var item = yerlesimler[i];
        elementSayilari[item.element]++;
        nitelikSayilari[item.nitelik]++;
        tablo += '<tr><td>' + item.ad + '</td><td>' + item.sembol + ' ' + item.burc + '</td><td>' + hcDhhFormat(item.derece, 2) + '°</td><td>' + item.element + '</td><td>' + item.nitelik + '</td></tr>';

        if (item.derece < 1 || item.derece > 29) {
            siniraYakin.push(item.ad);
        }
    }

    var gunes = yerlesimler[0];
    var ayKonum = yerlesimler[1];
    var yukselen = yerlesimler[2];
    var merkur = yerlesimler[3];
    var venus = yerlesimler[4];
    var mars = yerlesimler[5];
    var jupiter = yerlesimler[6];
    var saturn = yerlesimler[7];
    var baskinElement = hcDhhBaskinAnahtar(elementSayilari);
    var baskinNitelik = hcDhhBaskinAnahtar(nitelikSayilari);
    var acilar = hcDhhAciBul(yerlesimler);

    document.getElementById('hc-dhh-gunes').textContent = hcDhhYerlesimMetni(gunes);
    document.getElementById('hc-dhh-ay').textContent = hcDhhYerlesimMetni(ayKonum);
    document.getElementById('hc-dhh-yukselen').textContent = hcDhhYerlesimMetni(yukselen);
    document.getElementById('hc-dhh-tablo').innerHTML = tablo;
    document.getElementById('hc-dhh-oneri').innerHTML = hcDhhOneCikanKartlar(gunes, ayKonum, yukselen, baskinElement, baskinNitelik);
    document.getElementById('hc-dhh-elementler').innerHTML = hcDhhDengeKartlari(elementSayilari);
    document.getElementById('hc-dhh-nitelikler').innerHTML = hcDhhDengeKartlari(nitelikSayilari);

    document.getElementById('hc-dhh-karakter').textContent = hcDhhMetinBirlestir([
        'Temel üçlünüzde Güneş ' + gunes.burc + ', Ay ' + ayKonum.burc + ' ve yükselen ' + yukselen.burc + ' görünüyor.',
        hcDhhPlanetYorumu(gunes),
        hcDhhPlanetYorumu(yukselen),
        'Haritanın baskın elementi ' + baskinElement + ', baskın niteliği ise ' + baskinNitelik + ' olarak öne çıkıyor; bu kombinasyon olaylara yaklaşımınızda daha çok ' + HC_DHH_ELEMENT_YORUMLARI[baskinElement] + ' temalarını görünür kılabilir.'
    ]);

    document.getElementById('hc-dhh-duygu').textContent = hcDhhMetinBirlestir([
        hcDhhPlanetYorumu(ayKonum),
        hcDhhPlanetYorumu(venus),
        'Venüs ' + venus.burc + ' yerleşimi, ilişki içinde neyi çekici bulduğunuzu ve nasıl yakınlık kurduğunuzu gösterirken; Ay burcu duygusal güven ihtiyacınızı daha görünür hale getirir.',
        'Bu nedenle yakın ilişkilerde hem ' + ayKonum.profil.ozet + ' hem de ' + venus.profil.ozet + ' temaları birlikte çalışabilir.'
    ]);

    document.getElementById('hc-dhh-zihin').textContent = hcDhhMetinBirlestir([
        hcDhhPlanetYorumu(merkur),
        hcDhhPlanetYorumu(mars),
        'Merkür düşünme ve ifade biçimini, Mars ise hamle alma ve çatışma yönetimini anlatır.',
        'Bu ikili birlikte okunduğunda zihinsel hızınızın ve karar anındaki refleksinizin haritada nasıl çalıştığı daha net görülür.'
    ]);

    document.getElementById('hc-dhh-gelisim').textContent = hcDhhMetinBirlestir([
        hcDhhPlanetYorumu(jupiter),
        hcDhhPlanetYorumu(saturn),
        'Jüpiter genişleme ve fırsatları, Satürn ise yapı, sabır ve sınavları temsil eder.',
        'Bu iki yerleşim birlikte değerlendirildiğinde hem doğal büyüme alanınız hem de emek vererek olgunlaştırmanız gereken yaşam dersi daha görünür olur.'
    ]);

    document.getElementById('hc-dhh-acilar').innerHTML = hcDhhAciKartlari(acilar);

    var uyari = 'Gezegen konumları hızlı ön değerlendirme için hazırlanmış yaklaşık astronomik hesapla üretilir; profesyonel efemeris kadar hassas değildir.';
    uyari += ' İl seçimi il merkezinin koordinatlarını kullandığı için özellikle yükselen derecesinde küçük sapmalar oluşabilir.';
    if (siniraYakin.length) {
        uyari += ' ' + siniraYakin.join(', ') + ' burç sınırına yakın olduğu için doğum saati, UTC farkı veya ilçe düzeyindeki konum değişikliği sonucu etkileyebilir.';
    }

    document.getElementById('hc-dhh-uyari').textContent = uyari;
    document.getElementById('hc-dhh-result').classList.add('visible');
}
