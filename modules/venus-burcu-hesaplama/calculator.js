function hcVbhNormalize(derece) {
    var sonuc = derece % 360;
    return sonuc < 0 ? sonuc + 360 : sonuc;
}

function hcVbhNormalizeSigned(derece) {
    var sonuc = hcVbhNormalize(derece);
    return sonuc > 180 ? sonuc - 360 : sonuc;
}

function hcVbhRad(derece) {
    return derece * Math.PI / 180;
}

function hcVbhDeg(radyan) {
    return radyan * 180 / Math.PI;
}

function hcVbhFormat(sayi, hane) {
    return sayi.toLocaleString('tr-TR', {
        minimumFractionDigits: hane,
        maximumFractionDigits: hane
    });
}

function hcVbhJulianDay(yil, ay, gun, saat, dakika, utcFarki) {
    var utcMs = Date.UTC(yil, ay - 1, gun, saat, dakika, 0) - utcFarki * 60 * 60 * 1000;
    return utcMs / 86400000 + 2440587.5;
}

function hcVbhDunya(d) {
    var w = 282.9404 + 0.0000470935 * d;
    var e = 0.016709 - 0.000000001151 * d;
    var m = hcVbhNormalize(356.0470 + 0.9856002585 * d);
    var eAnomali = m + hcVbhDeg(e * Math.sin(hcVbhRad(m)) * (1 + e * Math.cos(hcVbhRad(m))));
    var xv = Math.cos(hcVbhRad(eAnomali)) - e;
    var yv = Math.sqrt(1 - e * e) * Math.sin(hcVbhRad(eAnomali));
    var v = hcVbhDeg(Math.atan2(yv, xv));
    var r = Math.sqrt(xv * xv + yv * yv);
    var lon = hcVbhNormalize(v + w);

    return {
        xs: r * Math.cos(hcVbhRad(lon)),
        ys: r * Math.sin(hcVbhRad(lon)),
        lon: lon
    };
}

function hcVbhVenusBoylami(jd) {
    var d = jd - 2451543.5;
    var dunya = hcVbhDunya(d);
    var el = {
        n: 76.6799 + 0.000024659 * d,
        i: 3.3946 + 0.0000000275 * d,
        w: 54.8910 + 0.0000138374 * d,
        a: 0.723330,
        e: 0.006773 - 0.000000001302 * d,
        m: hcVbhNormalize(48.0052 + 1.6021302244 * d)
    };
    var eAnomali = el.m + hcVbhDeg(el.e * Math.sin(hcVbhRad(el.m)) * (1 + el.e * Math.cos(hcVbhRad(el.m))));
    var j;

    for (j = 0; j < 4; j++) {
        eAnomali = eAnomali - (eAnomali - hcVbhDeg(el.e * Math.sin(hcVbhRad(eAnomali))) - el.m) / (1 - el.e * Math.cos(hcVbhRad(eAnomali)));
    }

    var xv = el.a * (Math.cos(hcVbhRad(eAnomali)) - el.e);
    var yv = el.a * Math.sqrt(1 - el.e * el.e) * Math.sin(hcVbhRad(eAnomali));
    var v = hcVbhDeg(Math.atan2(yv, xv));
    var r = Math.sqrt(xv * xv + yv * yv);
    var xh = r * (Math.cos(hcVbhRad(el.n)) * Math.cos(hcVbhRad(v + el.w)) - Math.sin(hcVbhRad(el.n)) * Math.sin(hcVbhRad(v + el.w)) * Math.cos(hcVbhRad(el.i)));
    var yh = r * (Math.sin(hcVbhRad(el.n)) * Math.cos(hcVbhRad(v + el.w)) + Math.cos(hcVbhRad(el.n)) * Math.sin(hcVbhRad(v + el.w)) * Math.cos(hcVbhRad(el.i)));

    return hcVbhNormalize(hcVbhDeg(Math.atan2(yh + dunya.ys, xh + dunya.xs)));
}

function hcVbhHareketMetni(hiz) {
    if (Math.abs(hiz) < 0.02) {
        return 'İstasyon çevresi';
    }

    return hiz < 0 ? 'Retro' : 'Direkt';
}

function hcVenusBurcuHesapla() {
    var tarih = document.getElementById('hc-vbh-tarih').value;
    var saat = document.getElementById('hc-vbh-saat').value;
    var utcFarki = parseFloat(document.getElementById('hc-vbh-utc').value);

    if (!tarih || !saat || isNaN(utcFarki)) {
        alert('Lütfen doğum tarihi, doğum saati ve UTC farkını girin.');
        return;
    }

    var tarihParca = tarih.split('-');
    var saatParca = saat.split(':');
    var yil = parseInt(tarihParca[0], 10);
    var ay = parseInt(tarihParca[1], 10);
    var gun = parseInt(tarihParca[2], 10);
    var saatDegeri = parseInt(saatParca[0], 10);
    var dakika = parseInt(saatParca[1], 10);

    if (yil < 1900 || yil > 2100) {
        alert('Bu hesaplayıcı 1900-2100 yılları arasındaki tarihler için uygundur.');
        return;
    }

    var jd = hcVbhJulianDay(yil, ay, gun, saatDegeri, dakika, utcFarki);
    var venusBoylami = hcVbhVenusBoylami(jd);
    var oncekiBoylam = hcVbhVenusBoylami(jd - 0.25);
    var sonrakiBoylam = hcVbhVenusBoylami(jd + 0.25);
    var gunlukHiz = hcVbhNormalizeSigned(sonrakiBoylam - oncekiBoylam) * 2;
    var burcIndex = Math.floor(venusBoylami / 30);
    var burcIciDerece = venusBoylami - burcIndex * 30;
    var burc = HC_VBH_BURCLAR[burcIndex];
    var hareket = hcVbhHareketMetni(gunlukHiz);
    var uyari = '';

    document.getElementById('hc-vbh-sembol').textContent = burc.sembol;
    document.getElementById('hc-vbh-burc').textContent = burc.ad;
    document.getElementById('hc-vbh-derece').textContent = hcVbhFormat(burcIciDerece, 2) + '° ' + burc.ad;
    document.getElementById('hc-vbh-ozet').textContent = burc.ozet;
    document.getElementById('hc-vbh-element').textContent = burc.element;
    document.getElementById('hc-vbh-nitelik').textContent = burc.nitelik;
    document.getElementById('hc-vbh-boylam').textContent = hcVbhFormat(venusBoylami, 2) + '°';
    document.getElementById('hc-vbh-hareket').textContent = hareket;
    document.getElementById('hc-vbh-sevgi').textContent = burc.sevgi;
    document.getElementById('hc-vbh-iliski').textContent = burc.iliski;
    document.getElementById('hc-vbh-cekim').textContent = burc.cekim;
    document.getElementById('hc-vbh-estetik').textContent = burc.estetik;
    document.getElementById('hc-vbh-golge').textContent = burc.golge;
    document.getElementById('hc-vbh-anahtar').textContent = burc.anahtar;

    if (burcIciDerece < 1 || burcIciDerece > 29) {
        uyari += 'Sonuç burç sınırına çok yakın. Doğum saati veya UTC farkındaki küçük değişimler Venüs burcunu etkileyebilir. ';
    }

    if (hareket === 'Retro') {
        uyari += 'Venüs retro göründüğü için ilişki tercihleri, değerler ve beğeniler daha içe dönük veya yeniden değerlendirme eğiliminde çalışabilir. ';
    } else if (hareket === 'İstasyon çevresi') {
        uyari += 'Venüs istasyon çevresinde görünüyor; derece değişimi yavaş olduğu için sınır etkileri ve yorum yoğunluğu artabilir. ';
    }

    uyari += 'Hesaplama yaklaşık astronomik Venüs boylamına göre yapılır ve hızlı astrolojik değerlendirme amacı taşır.';

    document.getElementById('hc-vbh-uyari').textContent = uyari;
    document.getElementById('hc-vbh-result').classList.add('visible');
}
